def add(a, b):
    """Add two numbers together"""
    return a + b

def subtract(a, b):
    """Subtract b from a"""
    return a - b

def multiply(a, b):
    result = a * b
    print(f"Result: {result}")
    return result

def divide(a, b):
    result = a / b
    print(f"Result: {result}")
    return result

